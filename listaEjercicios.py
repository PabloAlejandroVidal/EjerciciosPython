
#Te recomiendo empezar con proyectos sencillos que no involucren interfaces gráficas, 
# ya que te permitirán familiarizarte con los conceptos básicos de programación en Python. Aquí te propongo algunos:

#1 - Crear un programa que calcule el área y perímetro de un rectángulo, 
# pidiendo al usuario que ingrese los valores de base y altura.

#2 - Crear un programa que lea una cadena de texto y cuente cuántas veces aparece cada letra en la cadena.

#3 - Crear un juego de adivinanza de números, 
# donde el programa genera un número aleatorio y el usuario tiene que adivinarlo en un número limitado de intentos.

#4 - Crear un programa que lea un archivo de texto y cuente cuántas palabras hay en él.

#5 - Crear un programa que simule un juego de piedra, papel o tijera entre dos jugadores, 
# pidiendo que ingresen su elección y mostrando el resultado.

#6 - Crear un programa que calcule el factorial de un número ingresado por el usuario.

#7 - Crear un programa que lea una lista de números y devuelva la suma de todos los elementos.

#8 - Crear un programa que convierta un número decimal en binario.

#9 - Crear un programa que determine si un número ingresado por el usuario es primo o no.

#10 - Crear un programa que simule una calculadora básica, pidiendo al usuario que ingrese dos números y una operación (+, -, *, /) y devolviendo el resultado.

